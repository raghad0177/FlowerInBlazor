using LazurdIT.FluentOrm.Common;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BlazorApp3.Shared
{
    [FluentTable("Flower")]
    public class Flower : IFluentModel
    {
        [FluentField(name: "FlowerId", autoGenerated: true, isPrimary: true, allowNull: false)]
        public int FlowerId { get; set; }

        [FluentField(name: "Title", allowNull: false)]
        public string Title { get; set; } = string.Empty;

        [FluentField(name: "Description", allowNull: false)]
        public string Description { get; set; }

        public override string ToString()
        {
            return ($"Id : {FlowerId} , Title: {Title} , Description : {Description} ");
        }
    }
}
